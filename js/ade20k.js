// Build fast Map for O(1) lookup
const _colorMap = new Map(ADE20K.map(e => [e.label.toLowerCase().trim(), e.color]));

export function getLabelColor(label) {
  const key = label.toLowerCase().trim();

  // 1. Exact match
  if (_colorMap.has(key)) return _colorMap.get(key);

  // 2. Partial match
  for (const [k, v] of _colorMap) {
    if (k.includes(key) || key.includes(k)) return v;
  }

  // 3. Deterministic hash fallback — same unknown label always gets same color
  let h = 0;
  for (let i = 0; i < label.length; i++) h = label.charCodeAt(i) + ((h << 5) - h);
  return [Math.abs(h * 31) % 200 + 55, Math.abs(h * 17) % 200 + 55, Math.abs(h * 7) % 200 + 55];
}
